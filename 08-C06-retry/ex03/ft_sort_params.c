#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int		main(int argc, char **argv)
{
	(void)argc;
	(void)argv;
	ft_putstr(argv[1]);
	ft_putstr(argv[2]);
	ft_putstr(argv[3]);
	return (0);
}
